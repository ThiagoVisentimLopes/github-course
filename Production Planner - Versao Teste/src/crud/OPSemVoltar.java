    private void popularTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        try {
            ResultSet result = statement.executeQuery("SELECT * FROM maquina ORDER BY prioridade ASC");
            while (result.next()) {
                String linha[] = {String.valueOf(result.getInt(1)), String.valueOf(result.getInt(2)), String.valueOf(result.getInt(5)), result.getString(7), result.getString(3),result.getString(17),result.getString(16),result.getString(18),result.getString(19)};
                modelo.addRow(linha);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }

