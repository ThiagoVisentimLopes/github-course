    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        
        String status = jComboBox1.getSelectedItem().toString();
        int idInt = Integer.parseInt(jTextField1.getText());
        
        try {
            if (status.equals("")) {
                throw new Exception("Preencha o Status!");
            }

           
            statement.executeUpdate("UPDATE maquina SET status='" + status+ "' WHERE id_maquina=" + idInt);
            limparCampos();
            reiniciarBotoes();
            popularTabela();
            JOptionPane.showMessageDialog(this, "Informações da máquina alteradas com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
      
    }//GEN-LAST:event_jButton7ActionPerformed

