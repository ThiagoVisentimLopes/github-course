    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nPedido = jTextField2.getText();
        try {
            jButton7.setEnabled(true);
            if (nPedido.equals("")) {
                throw new Exception("Preencha com alguma informação");
            }
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            jTable1.clearSelection();
            for (int i = 0; i < modelo.getRowCount(); i++) {
                if (modelo.getValueAt(i, 4).toString().contains(nPedido)) {
                    jTable1.setRowSelectionInterval(i, i);
                    jTextField1.setText(modelo.getValueAt(i, 0).toString());
                    jTextField2.setText(modelo.getValueAt(i, 4).toString());
                    jTextField3.setText(modelo.getValueAt(i, 5).toString());
                    jComboBox1.setSelectedItem(modelo.getValueAt(i, 8).toString());
                    //reiniciarBotoes();
                    return;
                }
            }
            limparCampos();
            reiniciarBotoes();
            
            jTable1.clearSelection();
            throw new Exception("Não foi encontrado com esse Nº de Série!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
